# include <stdio.h>
int main()
{
    int a=10;
    a++; //Post increment operator
    printf("%d\n",a);
    ++a; //Pre increment operator
    printf("%d\n",a);

    a--; //Post decrement operator
    printf("%d\n",a);
    --a; //Pre decrement operator
    printf("%d",a);



   return 0;
}
